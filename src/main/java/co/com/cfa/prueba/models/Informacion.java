package co.com.cfa.prueba.models;

public class Informacion {
    private String mensajeHome;
    private String InformacionOficina;
    private String tituloAsociado;
    private String tituloFundacion;
    private String tituloEmpleo;

    public String getMensajeHome() {
        return mensajeHome;
    }

    public void setMensajeHome(String mensajeHome) {
        this.mensajeHome = mensajeHome;
    }

    public String getInformacionOficina() {
        return InformacionOficina;
    }

    public void setInformacionOficina(String informacionOficina) {
        InformacionOficina = informacionOficina;
    }

    public String getTituloAsociado() {
        return tituloAsociado;
    }

    public void setTituloAsociado(String tituloAsociado) {
        this.tituloAsociado = tituloAsociado;
    }

    public String getTituloFundacion() {
        return tituloFundacion;
    }

    public void setTituloFundacion(String tituloFundacion) {
        this.tituloFundacion = tituloFundacion;
    }

    public String getTituloEmpleo() {
        return tituloEmpleo;
    }

    public void setTituloEmpleo(String tituloEmpleo) {
        this.tituloEmpleo = tituloEmpleo;
    }
}
